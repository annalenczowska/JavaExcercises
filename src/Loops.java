import java.math.BigInteger;

public class Loops {
    public static void main(String[] args) {

        //pętla for
        for(int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        //pętla while
        int i = 0;
        while(i<100)
        {
            System.out.println(i);
            i++;
        }

        //pętla do while - zawsze wykonuje się co najmniej raz

        int j = 0;
        do { System.out.print(i);
            i++;
        }
            while (j <20);
    }


}
